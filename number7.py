TIP_RATE = 0.15
SALES_TAX_RATE = 0.095
cost = float(input("Enter meal cost please:"))

tip = cost * TIP_RATE
tax = cost * SALES_TAX_RATE
total = cost + tip + tax
print("tip:",round( tip, 2))
print("tax:",round( tax, 2))
print("total:",round( total, 2))

